        $token_fcm = $input['token_fcm'];
        $user_id = $input['user_id'];
        $user = User::findOrfail($user_id);
        $user->token_fcm = $token_fcm;
        $user->save();
        return response()->json([
            'success' => true,
            'message' => 'User update oke'
        ]);
    }

